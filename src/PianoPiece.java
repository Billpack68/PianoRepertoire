public class PianoPiece {
  private String name;
  private String author;
  private String arranger;
  private String book;
  private String genre;

  public PianoPiece(String name, String author, String arranger,
                    String book, String genre) {
    this.name=name;
    this.author = author;
    this.arranger = arranger;
    this.book = book;
    this.genre = genre;
  }
}
